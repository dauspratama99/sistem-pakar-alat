    <div style="text-align: justify;"> Masalah ini adalah masalah sepele yang setiap hari kita temui, jadi Anda harus bisa mengatasi sendiri. Masalah ini bisa disebabkan karena pada saat menaruh kertas pada laci kertas tidak rapi, kertas tidak <i>diketrek</i>/dilonggarkan dahulu, claw/kuku kotor dan <i>sponge</i> sudah aus/terkikis. Jika mesin fotocopy Anda mengalami Jam Paper maka Anda harus bisa mengatasinya sendiri dengan melihat layar LCD dan perhatikan kertas yang nyangkut di bagian mana.</div>
