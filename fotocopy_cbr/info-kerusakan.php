        <h3 id="3__Masalah_Korona_atas_dan_bawah"> 3. Masalah Korona atas dan bawah</h3>
        <div> Dalam menjaga kualitas hasil fotocopy yang selalu bagus maka Anda disarankan rutin membersihkan korona bagian atas dan korona bagian bawah, jika volume copying mesin fotocopy Anda sangat banyak maka Anda disarankan membersihkannya setiap hari, karena jika tidak maka korona atas dan korona bawah ini semakin hari semakin kotor. Tentu saja jika korona tersebut tidak dibersihkan secara rutin maka akan menyebabkan error E61 dan hasil cetakan menjadi jelek.</div>
        <div> <br />
