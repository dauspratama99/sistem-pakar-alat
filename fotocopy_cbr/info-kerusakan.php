    <h3 style="text-align: justify;" id="2__Clear_Error"> 2. Clear Error</h3>
    <div> Perlu Anda diketahui bahwasannya ada beberapa masalah pada mesin fotocopy yang hanya dengan melakukan Clear Error maka mesin fotocopy menjadi normal kembali dan tentu cara ini wajib Anda kuasai. adapun caranya sebagai berikut:</div>
    <div> <br />
    </div>
    <div>
      <div style="text-align: justify;"> 1.Tekan tombol *28* <i>(tanda * pada additional function)</i> pada panel fotocopy Anda (ketika menekan angka 2 dan 8 harus bersamaan)</div>
      <div style="text-align: justify;"> 2.Lalu tekan Copier &gt; Function &gt; Clear &gt; Err kemudian Ok</div>
      <div style="text-align: justify;"> 3.Kemudian matikan mesin Anda dan hidupkan lagi</div>
